package JavaIntro;

public class DataTypes {

	public static void main(String[] args) {
		
		       
		        byte myByte = 127;         // 8-bit signed integer
		        short myShort = 32767;     // 16-bit signed integer
		        int myInt = 2147483647;    // 32-bit signed integer
		        long myLong = 9223372036854775807L;  // 64-bit signed integer

		        float myFloat = 3.14f;     // 32-bit floating point
		        double myDouble = 3.14159265359;  // 64-bit floating point

		        char myChar = 'A';         // 16-bit Unicode character
		        boolean myBoolean = true;  // true or false

		        System.out.println(myByte);
		        System.out.println(myShort);
		        System.out.println(myInt);
		        System.out.println(myLong);

		        System.out.println(myFloat);
		        System.out.println(myDouble);

		        System.out.println(myChar);
		        System.out.println(myBoolean);

		        // Non-primitive Data Type (String)
		        
		        String myString = "Hello, Java!";
		        System.out.println(myString);
		    }
		


	}


