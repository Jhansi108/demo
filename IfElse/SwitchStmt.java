package Java;

public class SwitchStmt {

	public static void main(String[] args) {
		int a=4;
		switch (a) {
		case 0:
			System.out.println("number is 0");
			break;
		case 1:
			System.out.println("number is 1");
			break;
		case 2:
			System.out.println("number is 2");
			break;
		case 3:
			System.out.println("number is 3");
			break;
			default:
				System.out.println(a);
		
		}
	}

}
